// https://adventofcode.com/2016/day/8

#include <bits/stdc++.h>

#define all(x) begin(x), end(x)
#define sz(x) ((int)(x).size())
using namespace std;

void solve(istream& ins)
{
	assert(ins.good());
	auto rect_pat = regex {R"(rect (\d+)x(\d+))"};
	auto rotr_pat = regex {R"(rotate row y=(\d+) by (\d+))"};
	auto rotc_pat = regex {R"(rotate column x=(\d+) by (\d+))"};
	auto screen = vector<vector<char>>(6, vector<char>(50, '.'));
	for (string line; getline(ins, line);) {
		smatch m;
		if (regex_match(line, m, rect_pat)) {
			int A = stoi(m[1]), B = stoi(m[2]);
			for (int r = 0; r < B; ++r) {
				for (int c = 0; c < A; ++c) {
					screen[r][c] = '#';
				}
			}
		} else if (regex_match(line, m, rotr_pat)) {
			int A = stoi(m[1]), B = stoi(m[2]);
			rotate(rbegin(screen[A]), rbegin(screen[A]) + B, rend(screen[A]));
		} else if (regex_match(line, m, rotc_pat)) {
			int A = stoi(m[1]), B = stoi(m[2]);
			for (int _ = 0; _ < B; ++_) {
				for (int i = 0; i < sz(screen) - 1; ++i) {
					swap(screen[i][A], screen.back()[A]);
				}
			}
		} else { assert(false); }
	}
	auto cnt_lit = [](int p, auto& row) { return p + count(all(row), '#'); };
	cout << accumulate(all(screen), 0, cnt_lit) << '\n';
	for (auto& row : screen) {
		for (char c : row) { cout << c; }
		cout << '\n';
	}
}

