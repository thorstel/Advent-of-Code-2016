#include <bits/stdc++.h>

#define all(x) begin(x), end(x)
#define sz(x) ((int)(x).size())
using namespace std;

void solve(istream& ins)
{
	assert(ins.good());
	vector<string> grid;
	for (string line; getline(ins, line);)
		grid.push_back(line);

	int num_loc = 0, start_row = 0, start_col = 0;
	for (int r = 0; r < sz(grid); ++r) {
		for (int c = 0; c < sz(grid[r]); ++c) {
			if (grid[r][c] != '.' && grid[r][c] != '#')
				++num_loc;
			if (grid[r][c] == '0') {
				start_row = r;
				start_col = c;
			}
		}
	}

	const vector<pair<int, int>> offsets {{0, 1}, {0, -1}, {1, 0}, {-1, 0}};
	set<tuple<int, int, vector<bool>>> seen;
	deque<tuple<int, int, int, vector<bool>>> Q;
	vector<bool> initial_state(num_loc, false);
	initial_state[0] = true;
	Q.emplace_back(make_tuple(0, start_row, start_col, initial_state));
	seen.emplace(make_tuple(start_row, start_col, initial_state));
	bool p1_ans = false;
	while (!Q.empty()) {
		auto [steps, row, col, state] = Q.front();
		Q.pop_front();
		// Part 1
		if (!p1_ans && all_of(all(state), [](bool x) { return x; })) {
			cout << steps << endl;
			p1_ans = true;
		}
		// Part 2
		if (all_of(all(state), [](bool x) { return x; }) && grid[row][col] == '0') {
			cout << steps << endl;
			break;
		}
		++steps;
		for (auto [dr, dc] : offsets) {
			int r = row + dr;
			int c = col + dc;
			if (r < 0 || r >= sz(grid) || c < 0 || c >= sz(grid[0]) || grid[r][c] == '#')
				continue;
			auto new_state = state;
			if (grid[r][c] != '.')
				new_state[grid[r][c] - '0'] = true;
			if (seen.emplace(make_tuple(r, c, new_state)).second)
				Q.emplace_back(make_tuple(steps, r, c, new_state));
		}
	}
}

